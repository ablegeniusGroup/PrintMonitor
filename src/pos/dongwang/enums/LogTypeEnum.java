public enum LogTypeEnum {
    KVSPRINT("KVS出菜时间","KVSP"),STBL("开台","STBL"),CTBL("转台","CTBL");

    private String name;
    private String value;

    LogTypeEnum(String name, String value){
        this.name=name;
        this.value=value;
    }

    public String getName() {
        return name;
    }

    public String getValue() {
        return value;
    }
}
